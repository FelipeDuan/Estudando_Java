package secao02;

// Variáveis
public class Variaveis {
    @SuppressWarnings("unused")
    public static void main(String[] args) {
        // Declaração de variável (nome minuscúlo, iniciar com letras)
        int valor;

        // Declaração de variáveis (mais de uma)
        int num1, num2, num3;

        // Declarando e inicializando algumas variáveis
        int num4, num5 = 4, num6 = 7, num8;

        // Tipos diferentes de Variáveis (para uma maior organização, um abaixo do outro)
        char caractere;
        float preco;

        // Declarando
        int num9;

        // Declarando e inicializando
        int num10 = 99;

        // Por padrão, os valores de ponto flutuante são double no Java
        float valor2 = 34.2f;
        double valor3 = 34.2;

        // Errado
        //int 10eu = 7;
    }
}
