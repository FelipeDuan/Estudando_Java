package Udemy.CursoJava.secao08;

@SuppressWarnings("ALL")
public class OrientacaoObjetos {
    /*
        Orientação a objeto -> Paradigma da Programação
        - Uma forma de criar/desenvolver programas
        Analogia: Massinhas coloridas colocadas em um molde

        Para entender a Orientação a Objeto:
        - A "Classe" é o molde, a partir desse molde é possível criar diferentes objetos
        com características semelhantes
        Ex: Classe Udemy.CursoJava.secao08.Produto, com características como: nome, preço e desconto (chamados de Atributos)
        Classe Udemy.CursoJava.secao08.Produto -> Características/Atributos: nome, preço e desconto

        - Para compreender os outros elementos, imagine que cada classe pode executar algumas ações
        tendo alguns comportamentos
        Ex: Na Classe Udemy.CursoJava.secao08.Produto, existe um comportamento/ação de "Aumentar o Preço", isso é chamado de Método
        da classe.
        Os Métodos da classe são as ações que a classe realiza, como "aumentar o preço" ou "aumentar o desconto",
        eles são semelhantes às funções, são praticamente o mesmo, mas a diferença reside no fato de que a função
        não é dependente de nenhuma classe (nenhum molde).
        Já o Método, é específico de uma classe, exemplo: "Aumentar Preço" é específico da classe Udemy.CursoJava.secao08.Produto.

        Na Orientação a Objeto:
        A partir do molde, são criados diferentes Udemy.CursoJava.secao08.Objetos, aqui chamados de "Instâncias". Objeto == Instância
        Ex: Classe Udemy.CursoJava.secao08.Produto cria: (Notebook - R$2.300,00 - 15%) e (Caneta BIC - R$2,94 - 5%) -> Udemy.CursoJava.secao08.Objetos/Instâncias

        De forma superficial, a fins didáticos, isso é Orientação a Objeto!
     */
}
