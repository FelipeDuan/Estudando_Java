package Udemy.CursoJava.JavaLang;
// Cálculos com a Math
public class Calculos {
    public static void main(String[] args) {
        System.out.println(Math.E); // Constante de Euler = 2.71...

        System.out.println(Math.PI); // Constate Pi = 3,14...

        System.out.println(Math.sin(45)); // Cálculo do Seno

        System.out.println(Math.cos(45)); // Cálculo do Cosseno

        System.out.println(Math.tan(45)); // Cálculo da Tangente

        System.out.println(Math.log(8)); // Logarítmo

        System.out.println(Math.round(Math.PI)); // Arredonda para o int mais próximo

        System.out.println(Math.ceil(Math.PI)); // Arredonda para o de cima mais próximo

        System.out.println(Math.floor(Math.PI)); // Arredonda para o de baixo mais próximo

        System.out.println(Math.pow(3,2)); // 3 elevado a 2

        System.out.println(Math.sqrt(9)); // Raiz quadrada de 9

        System.out.println(Math.cbrt(8)); // Raiz cúbica de 8
    }
}
